# Importamos la librería NumPy
import numpy as np

# Creamos un vector (array) con los valores indicados
vector = np.array([1, 2, 3, 4, 5])

# Mostramos el vector completo
print("Vector:", vector)

# Mostramos el número de elementos del vector
print("Tamaño:", vector.size)

# Calculamos y mostramos la suma de todos los elementos
print("Suma:", np.sum(vector))